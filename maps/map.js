// Key - value pair 

const myMap = new Map();

myMap.set("name", "shelly");

myMap.set("age", 10);


console.log(myMap.get("name"));

console.log(myMap.has("name")); // Returns true

myMap.size; // returns the size of the map;

myMap.delete("name"); // Deletes the key - value pair of the key "age"

